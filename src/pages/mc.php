    <h1>You made it!</h1>
    <p>Setup is quite easy.</p>
    <p>Make sure you have the Curseforge client. If not, you can download it <a href="https://download.curseforge.com/">here</a>.</p>
    <p>After that, download the modpack profile <a href="/public/zip/Better_Minecraft_for_Meatballs-v69.zip">here</a>.</p>
    <p>Once downloaded, open Curseforge and click the button circled in red below.</p>
    <img src="/public/images/Overwolf_7SGZHL27wv.png" alt="A red circle around the button with text '+ Create New Profile'.">
    <p>Then click on the underlined text that says "import" as shown below.</p>
    <img src="/public/images/Overwolf_CNWpPaAUgb.png" alt="A red circle around the underlined text at the top of the screen reading 'import'.">
    <p>Then find and select the .zip file you downloaded. After installing, you should be able to click 'Play'.</p>
    <p>I <b>HIGHLY</b> suggest increasing allocated RAM if you have more than 8GB.</p>
    <p>To do so, select 'Installations' at the top of the Minecraft Launcher.</p>
    <p>Then select the three dots button next to 'Better Minecraft for Meatballs' and select the 'Edit' option.</p>
    <p>After that, scroll down and select 'More Options' button.</p>
    <p>Scroll down further until you can see the 'JVM OPTIONS' line and change the <code>-Xmx</code> and <code>-Xms</code> variables to larger numbers.</p>
    <p>If it matters, <code>Xms</code> is what Java starts out using and <code>Xmx</code> is the maximum that Java can use.</p>
    <p>Set those options to something suitable for your device.</p>
    <p>For reference, I have 16GB of RAM and have mine set to <code>-Xmx7168m -Xms6656m</code>.</p>
    <br>
    <p>You are now at a point where you can run the modpack! It is also advisable to read what follows, and if you ask about something that is covered below, I will blatantly ignore you! :D</p>
    <br>
