    <p>A mod makes the leaves not render after a certain depth and it's kinda dumb.</p>
    <h4>TO FIX:</h4>
    <p>From the main menu of Minecraft, select the scroll button on the right of the Bisect ad (stupid ads) as shown below.</p>
    <p>(If you are in-game, it's the big 'Mods' button in the pause menu.)</p>
    <img src="/public/images/javaw_46EqwfyxzJ.jpg" alt="If you read this, that's kinda cool.">
    <p>In the search bar, type 'cull' and select 'Cull Less Leaves'.</p>
    <p>Then select the weird optoins button in the top right, and you have options to disable it completely, or change the depth of leaves that are rendered.</p>
